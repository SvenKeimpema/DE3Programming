      <h4>Dit is de FW44 van Williams voor het Formule 1-seizoen van 2022</h4>
      <p class="textHeader">Williams heeft de FW44 onthuld voor het Formule 1-seizoen van 2022. De nieuwe wagen van Alexander Albon en Nicholas Latifi moet ervoor zorgen dat het team
        eindelijk weer competitief wordt. Vooralsnog lijkt de wagen die gepresenteerd is, wel om een showcar te gaan. De echte details worden dus nog niet vrijgegeven.
      </p><br><br>
      <p>Williams is een grote naam in de Formule 1. Opgericht door Frank Williams, die in 2021 overleed, werkte het team zich op naar de top van de sport en behaalde het grote successen.
        De laatste jaren is er van die successen weinig meer over. De familie Williams vertrok daarnaast bij het team, maar de naam bleef boven de gevel staan.
      </p><br><br>
      <p>Williams werd in 2020 namelijk gekocht door Dorilton Capital, een investeringsmaatschappij uit Amerika. Die kozen voor een nieuwe weg binnen het team. Er is eindelijk weer meer
        geld beschikbaar, de fabriek wordt opgeknapt en de oude methodes zijn aan de kant geschoven. Alles in het teken van een mooiere toekomst.
      </p><br><br>
      <p>George Russell vertrok echter bij het team uit Grove om in 2022 voor Mercedes te rijden. Dat team levert ook in 2022 de motor van Williams. Alexander Albon werd als vervanger
        gehaald voor Russell. De Thai stond een jaar langs de kant, maar speelde wel een cruciale rol voor Red Bull Racing in de titelstrijd van Max Verstappen.
      </p>
